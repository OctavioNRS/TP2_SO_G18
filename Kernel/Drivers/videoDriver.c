/*
 * videoDriver.c — Driver de video VESA con framebuffer lineal.
 *
 * Lee la estructura VBE Mode Info que dejó Pure64 en memoria fija para conocer
 * resolución, BPP y dirección del framebuffer. Expone primitivas para dibujar
 * píxel, rectángulo, círculo, glifo (font 8x16 en .rodata) y string. `scrollUp`
 * hace memcpy hacia arriba y rellena la franja inferior con el color de fondo.
 */
#include <videoDriver.h>
#include <stdint.h>

struct vbe_mode_info_structure {
	uint16_t attributes;		// deprecated, only bit 7 should be of interest to you, and it indicates the mode supports a linear frame buffer.
	uint8_t window_a;			// deprecated
	uint8_t window_b;			// deprecated
	uint16_t granularity;		// deprecated; used while calculating bank numbers
	uint16_t window_size;
	uint16_t segment_a;
	uint16_t segment_b;
	uint32_t win_func_ptr;		// deprecated; used to switch banks from protected mode without returning to real mode
	uint16_t pitch;			// number of bytes per horizontal line
	uint16_t width;			// width in pixels
	uint16_t height;			// height in pixels
	uint8_t w_char;			// unused...
	uint8_t y_char;			// ...
	uint8_t planes;
	uint8_t bpp;			// bits per pixel in this mode
	uint8_t banks;			// deprecated; total number of banks in this mode
	uint8_t memory_model;
	uint8_t bank_size;		// deprecated; size of a bank, almost always 64 KB but may be 16 KB...
	uint8_t image_pages;
	uint8_t reserved0;
 
	uint8_t red_mask;
	uint8_t red_position;
	uint8_t green_mask;
	uint8_t green_position;
	uint8_t blue_mask;
	uint8_t blue_position;
	uint8_t reserved_mask;
	uint8_t reserved_position;
	uint8_t direct_color_attributes;
 
	uint32_t framebuffer;		// physical address of the linear frame buffer; write here to draw to the screen
	uint32_t off_screen_mem_off;
	uint16_t off_screen_mem_size;	// size of memory in the framebuffer but not being displayed on the screen
	uint8_t reserved1[206];
} __attribute__ ((packed));

typedef struct vbe_mode_info_structure * VBEInfoPtr;

VBEInfoPtr VBE_mode_info = (VBEInfoPtr) 0x0000000000005C00;

static uint8_t *auxFramebuffer = 0;
static uint32_t auxBuffWidth = 0;
static uint32_t auxBuffHeight = 0;

uint16_t getScreenWidth() {
	if (auxFramebuffer == 0) return VBE_mode_info->width;
    return auxBuffWidth;
}

uint16_t getScreenHeight() {
	if (auxFramebuffer == 0) return VBE_mode_info->height;
    return auxBuffHeight;
}

int withinScreenBounds(uint64_t x, uint64_t y) {
    return (x < getScreenWidth() && y < getScreenHeight());
}

uint8_t *getFrameBuffer() {
    if (auxFramebuffer == 0) return (uint8_t *)(uint64_t) VBE_mode_info->framebuffer;
    return auxFramebuffer;
}

void setAuxFramebuffer(uint8_t *buffer, uint32_t width, uint32_t height) {
    auxFramebuffer = buffer;
    if (buffer == 0) {
        auxBuffWidth = auxBuffHeight = 0;
        return;
    }
    auxBuffWidth = width;
    auxBuffHeight = height;
}

void renderAux() {
    if (auxFramebuffer == 0) return;
    
    uint8_t *framebuffer = (uint8_t *)(uint64_t) VBE_mode_info->framebuffer;
    uint64_t copyWidth = auxBuffWidth < VBE_mode_info->width ? auxBuffWidth : VBE_mode_info->width;
    uint64_t copyHeight = auxBuffHeight < VBE_mode_info->height ? auxBuffHeight : VBE_mode_info->height;
    
    // Calculo los tamaños de las filas en bytes (3 bytes por pixel para BGR)
    uint64_t copyRowBytes = copyWidth * 3;
    uint64_t auxRowBytes = auxBuffWidth * 3;
    uint64_t fbRowBytes = VBE_mode_info->width * 3;
    
    // Copio fila por fila usando copia de memoria directa
    for (uint64_t y = 0; y < copyHeight; y++) {
        uint8_t *auxRow = auxFramebuffer + (y * auxRowBytes);
        uint8_t *fbRow = framebuffer + (y * fbRowBytes);
        
        // Copio la fila entera - optimizado para chunks de 8 bytes cuando es posible
        uint64_t *src64 = (uint64_t*)auxRow;
        uint64_t *dst64 = (uint64_t*)fbRow;
        uint64_t chunks = copyRowBytes / 8;
        uint64_t remainder = copyRowBytes % 8;
        
        // Copio chunks de 8 bytes
        for (uint64_t i = 0; i < chunks; i++) {
            *dst64++ = *src64++;
        }
        
        // Copio los bytes restantes
        uint8_t *src8 = (uint8_t*)src64;
        uint8_t *dst8 = (uint8_t*)dst64;
        for (uint64_t i = 0; i < remainder; i++) {
            *dst8++ = *src8++;
        }
    }
}

uint64_t getVideoOffset(uint64_t x, uint64_t y) {
    if (auxFramebuffer == 0) return (x * ((VBE_mode_info->bpp)/8)) + (y * VBE_mode_info->pitch);
    return (x + y * auxBuffWidth)*3;
}

static uint64_t alphaBlend(uint32_t fgColor, uint32_t bgColor, uint8_t alpha) {
    if (alpha == 0) return bgColor;
    if (alpha == 0xFF) return fgColor;
    

    uint8_t fgR = (fgColor >> 16) & 0xFF;
    uint8_t fgG = (fgColor >> 8) & 0xFF;
    uint8_t fgB = fgColor & 0xFF;

    uint8_t bgR = (bgColor >> 16) & 0xFF;
    uint8_t bgG = (bgColor >> 8) & 0xFF;
    uint8_t bgB = bgColor & 0xFF;

    uint8_t outR = (fgR * alpha + bgR * (0xFF - alpha)) / 0xFF;
    uint8_t outG = (fgG * alpha + bgG * (0xFF - alpha)) / 0xFF;
    uint8_t outB = (fgB * alpha + bgB * (0xFF - alpha)) / 0xFF;

    uint64_t color = 0 | (outR << 16) | (outG << 8) | outB;
    return color;
}

static uint64_t blendPixel(uint32_t colorARGB, uint64_t x, uint64_t y) {
    if (!withinScreenBounds(x, y)) return 0;
    uint8_t alpha = (colorARGB >> 24) & 0xFF;
    if (alpha == 0) alpha = 0xFF;
    uint32_t bgColor = readPixel(x, y);
    uint64_t hexColor = colorARGB & 0x00FFFFFF;
    return alphaBlend(hexColor, bgColor, alpha);
}

// hexColor: ARGB. A=0 default a 100% opacidad
void putPixel(uint32_t hexColor, uint64_t x, uint64_t y) {
    if (!withinScreenBounds(x, y)) return;
    hexColor = blendPixel(hexColor, x, y);
    uint8_t * framebuffer = getFrameBuffer();
    uint64_t offset = getVideoOffset(x, y);
    framebuffer[offset]     =  (hexColor) & 0xFF; //B
    framebuffer[offset+1]   =  (hexColor >> 8) & 0xFF; //G 
    framebuffer[offset+2]   =  (hexColor >> 16) & 0xFF; //R
}

uint32_t readPixel(uint64_t x, uint64_t y) {
    if (!withinScreenBounds(x, y)) return 0;
    uint8_t * framebuffer = getFrameBuffer();
    uint64_t offset = getVideoOffset(x, y);
    uint32_t color = 0;
    color |= framebuffer[offset];
    color |= framebuffer[offset+1] << 8;
    color |= framebuffer[offset+2] << 16;
    return color;
}

void drawRectangle(uint32_t hexColor, uint64_t x, uint64_t y, uint64_t width, uint64_t height) {
	for (int i = 0; i < width; i++) {
		for (int j = 0; j < height; j++) {
			putPixel(hexColor, x + i, y + j);
		}
	}
}

void drawCircle(uint32_t hexColor, int64_t centerX, int64_t centerY, uint64_t radius) {
    int64_t rSquared = radius * radius;
    int64_t startX = centerX - radius;
    int64_t startY = centerY - radius;
    int64_t endX = centerX + radius;
    int64_t endY = centerY + radius;
    
    for (int64_t x = startX; x <= endX; x++) {
        for (int64_t y = startY; y <= endY; y++) {
            if ((x-centerX)*(x-centerX) + (y-centerY)*(y-centerY) < rSquared) {
                putPixel(hexColor, x, y);
            }
        }
    }
}

void drawCircumference(uint32_t hexColor, int64_t centerX, int64_t centerY, uint64_t radius) {
	int64_t x = radius;
	int64_t y = 0;
	int64_t radiusError = 1 - x;

	while (x >= y) {
		putPixel(hexColor, centerX + x, centerY + y);
        putPixel(hexColor, centerX - x, centerY + y);
        putPixel(hexColor, centerX + y, centerY + x);
        putPixel(hexColor, centerX - y, centerY + x);
        putPixel(hexColor, centerX + x, centerY - y);
        putPixel(hexColor, centerX - x, centerY - y);
        putPixel(hexColor, centerX + y, centerY - x);
        putPixel(hexColor, centerX - y, centerY - x);
		y++;
		if (radiusError < 0) {
			radiusError += 2 * y + 1;
		} else {
			x--;
			radiusError += 2 * (y - x) + 1;
		}
	}
}

void setBackground(uint32_t color) {
    uint64_t width = getScreenWidth();
    uint64_t height = getScreenHeight();
    uint8_t *framebuffer = getFrameBuffer();
    uint8_t blue = color & 0xFF;
    uint8_t green = (color >> 8) & 0xFF;
    uint8_t red = (color >> 16) & 0xFF;

    for (uint64_t y = 0; y < height; y++) {
        for (uint64_t x = 0; x < width; x++) {
            uint64_t offset = (y*width + x)*3;
            framebuffer[offset] = blue;
            framebuffer[offset+1] = green;
            framebuffer[offset+2] = red;
        }
    }
}

